package All_basics.Operators;

public class Relational {
    public static void main(String[] args) {
        int num1 = 5;
        int num2 = 10;
        System.out.println("num1<num is " + (num1<num2) );
        System.out.println("num1>num2 is " + (num1>num2) );
        System.out.println("num1<=num2 is " + (num1<=num2));
        System.out.println("num1>=num2 is " + (num1>=num2));
        System.out.println("num1==num2 is " + (num1==num2) );
        System.out.println("num1!=num2 is " + (num1!=num2) );
    }
}