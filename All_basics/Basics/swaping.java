package All_basics.Basics;

public class swaping {
    public static void main(String[] args) {
        int num1=5; int num2=7; int swap;
        System.out.println("before swapping "+ num1 + " " + num2);
        swap = num1;
        num1=num2;
        num2=swap;
        System.out.println("after swapping "+ num1 + " " + num2);

    }
}
